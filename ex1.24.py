# Crie um programa que calcule e mostre a área de um triângulo retângulo dadas as medidas dos seus catetos como entrada.

cateto1 = float(input("Digite o comprimento do primeiro cateto: "))
cateto2 = float(input("Digite o comprimento do segundo cateto: "))


area = (cateto1 * cateto2) / 2

print("A área do triângulo retângulo é:", area)
